            ? rinascente_company_profile_data()
            : array();
          $company_name = trim( (string) ( $company_data['company_name'] ?? get_theme_mod( 'company_name', '' ) ) );
          $company_rows = array();

          if ( '' !== trim( (string) ( $company_data['company_name_en'] ?? '' ) ) ) {
            $company_rows[] = array(
              'label' => '会社名（英語）',
              'type'  => 'text',
              'value' => $company_data['company_name_en'],
            );
          }

          if ( '' !== trim( (string) ( $company_data['company_ceo'] ?? '' ) ) ) {
            $company_rows[] = array(
              'label' => '代表者',
              'type'  => 'text',
              'value' => '代表取締役 ' . $company_data['company_ceo'],
            );
          }

          foreach (
            array(
              'company_founded' => '設立',
              'company_capital' => '資本金',
              'company_address' => '所在地',
            ) as $key => $label
          ) {
            $value = trim( (string) ( $company_data[ $key ] ?? '' ) );
            if ( '' === $value ) {
              continue;
            }

            $company_rows[] = array(
              'label' => $label,
              'type'  => 'text',
              'value' => $value,
            );
          }

          foreach (
            array(
              'company_tel'   => '電話番号',
              'company_fax'   => 'FAX番号',
            ) as $key => $label
          ) {
            $value = trim( (string) ( $company_data[ $key ] ?? '' ) );
            if ( '' === $value ) {
              continue;
            }

            $company_rows[] = array(
              'label' => $label,
              'type'  => in_array( $key, array( 'company_tel', 'company_fax' ), true ) ? 'tel' : 'email',
              'value' => $value,
            );
          }

          foreach (
            array(
              'company_business' => '事業内容',
              'company_products' => '主要製品',
              'company_hours'    => '受付時間',
            ) as $key => $label
          ) {
            $value = trim(
              (string) (
                'company_products' === $key && function_exists( 'rinascente_company_products_text' )
                  ? rinascente_company_products_text()
                  : ( $company_data[ $key ] ?? '' )
              )
            );
            if ( '' === $value ) {
              continue;
            }

            $company_rows[] = array(
              'label' => $label,
              'type'  => in_array( $key, array( 'company_business', 'company_products' ), true ) ? 'multiline' : 'text',
              'value' => $value,
            );
          }
